"""
Sends the daily strategic digest via Gmail SMTP using an App Password.

Setup (one-time):
  1. Google Account → Security → 2-Step Verification (enable if not already)
  2. Google Account → Security → App passwords
  3. Generate a password for "Mail" → copy it into .env as GMAIL_APP_PASSWORD
"""

import os
import re
import smtplib
from datetime import date
from email.mime.multipart import MIMEMultipart
from email.mime.text import MIMEText


def send(briefing: str, notion_url: str = "") -> None:
    sender = os.environ["GMAIL_ADDRESS"]
    recipient = os.environ.get("DIGEST_RECIPIENT", sender)
    password = os.environ["GMAIL_APP_PASSWORD"]

    today = date.today().strftime("%B %d, %Y")
    subject = f"Your Strategic Digest — {today}"

    html = _to_html(briefing, notion_url, today)
    plain = briefing

    msg = MIMEMultipart("alternative")
    msg["Subject"] = subject
    msg["From"] = f"Strategic Digest <{sender}>"
    msg["To"] = recipient
    msg.attach(MIMEText(plain, "plain"))
    msg.attach(MIMEText(html, "html"))

    with smtplib.SMTP_SSL("smtp.gmail.com", 465) as server:
        server.login(sender, password)
        server.sendmail(sender, recipient, msg.as_string())

    print(f"Email sent to {recipient}")


