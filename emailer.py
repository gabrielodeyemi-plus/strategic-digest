def _to_html(markdown: str, notion_url: str, today: str) -> str:
    body_html = _md_to_html(markdown)

    notion_block = ""
    if notion_url:
        notion_block = f'<a class="notion-btn" href="{notion_url}">📄 View full briefing in Notion →</a>'

    return f"""<!DOCTYPE html>
<html><head><meta charset="UTF-8">
<style>{_CSS}</style></head>
<body><div class="wrap">
<div class="header">
  <h1>Strategic Intelligence Digest</h1>
  <p>{today}</p>
</div>
{body_html}
{notion_block}
<div class="footer">Generated by your Strategic Digest agent.</div>
</div></body></html>"""


def _md_to_html(text: str) -> str:
    lines = text.splitlines()
    html_lines = []
    in_ul = False

    def close_ul():
        nonlocal in_ul
        if in_ul:
            html_lines.append("</ul>")
            in_ul = False

    for line in lines:
        stripped = line.rstrip()

        if stripped.startswith("## "):
            close_ul()
            html_lines.append(f"<h2>{_inline(stripped[3:])}</h2>")
        elif stripped.startswith("### "):
            close_ul()
            html_lines.append(f"<h3>{_inline(stripped[4:])}</h3>")
        elif stripped.startswith("---"):
            close_ul()
            html_lines.append("<hr>")
        elif stripped.startswith("- ") or stripped.startswith("• "):
            if not in_ul:
                html_lines.append("<ul>")
                in_ul = True
            html_lines.append(f"<li>{_inline(stripped[2:])}</li>")
        elif stripped == "":
            close_ul()
        else:
            close_ul()
            html_lines.append(f"<p>{_inline(stripped)}</p>")

    close_ul()
    return "\n".join(html_lines)


def _inline(text: str) -> str:
    text = re.sub(r"\*\*([^*]+)\*\*", r"<strong>\1</strong>", text)
    text = re.sub(r"\*([^*]+)\*", r"<em>\1</em>", text)
    return text
